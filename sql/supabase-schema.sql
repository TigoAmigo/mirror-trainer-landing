create extension if not exists pgcrypto;

create table if not exists public.leads (
  id uuid primary key default gen_random_uuid(),
  created_at timestamptz not null default timezone('utc', now()),
  name text not null,
  contact text not null,
  phone text,
  telegram text,
  email text,
  comment text,
  page_path text,
  source_context text,
  session_id text,
  purchase_intent_choice text,
  interest_choice text,
  utm_source text,
  utm_medium text,
  utm_campaign text,
  device_type text,
  referrer text
);

alter table public.leads add column if not exists phone text;
alter table public.leads add column if not exists telegram text;

create table if not exists public.feedback_votes (
  id uuid primary key default gen_random_uuid(),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  session_id text not null unique,
  interest_level text not null,
  page_path text,
  utm_source text,
  utm_medium text,
  utm_campaign text,
  device_type text,
  referrer text,
  constraint feedback_votes_interest_level_check
    check (interest_level in ('Очень интересно', 'Скорее интересно', 'Нейтрально', 'Неинтересно'))
);

create table if not exists public.purchase_intent (
  id uuid primary key default gen_random_uuid(),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  session_id text not null unique,
  purchase_choice text not null,
  page_path text,
  utm_source text,
  utm_medium text,
  utm_campaign text,
  device_type text,
  referrer text,
  constraint purchase_intent_choice_check
    check (
      purchase_choice in (
        'Да, купил(а) бы',
        'Возможно, если будут отзывы / подробности',
        'Возможно, если будет ниже цена',
        'Нет'
      )
    )
);

create table if not exists public.event_logs (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default timezone('utc', now()),
  session_id text,
  event_name text not null,
  page_path text,
  section_id text,
  label text,
  utm_source text,
  utm_medium text,
  utm_campaign text,
  device_type text,
  referrer text,
  metadata jsonb not null default '{}'::jsonb
);

create table if not exists public.admin_users (
  email text primary key,
  created_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_leads_created_at on public.leads (created_at desc);
create index if not exists idx_leads_session_id on public.leads (session_id);
create index if not exists idx_feedback_votes_interest_level on public.feedback_votes (interest_level);
