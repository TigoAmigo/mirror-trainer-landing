create or replace function public.get_dashboard_snapshot()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  result jsonb;
begin
  perform public.assert_admin_access();

  with summary as (
    select
      (select count(*) from public.leads) as total_leads,
      (select count(distinct session_id) from public.event_logs where event_name = 'page_view') as unique_visitors,
      (select count(*) from public.event_logs where event_name = 'cta_click') as cta_clicks
  ),
  cta_breakdown as (
    select jsonb_agg(
      jsonb_build_object('label', labels.label, 'count', coalesce(counts.total, 0))
      order by labels.sort_order
    ) as data
    from (
      values
        ('hero_primary', 1),
        ('hero_secondary', 2),
        ('hero_quick_product', 3),
        ('hero_quick_training', 4),
        ('header_preorder', 5)
    ) as labels(label, sort_order)
    left join lateral (
      select count(*) as total
      from public.event_logs
      where event_name = 'cta_click'
        and label = labels.label
    ) counts on true
  ),
  scroll_breakdown as (
    select jsonb_agg(
      jsonb_build_object(
        'label', concat(depths.depth, '%'),
        'depth', depths.depth,
        'count', coalesce(counts.total, 0)
      )
      order by depths.depth
    ) as data
    from (
      values (25), (50), (75), (100)
    ) as depths(depth)
    left join lateral (
      select count(*) as total
      from public.event_logs
      where event_name = 'scroll_depth'
        and coalesce((metadata ->> 'depth')::int, 0) = depths.depth
    ) counts on true
  )
  select jsonb_build_object(
    'updatedAt', timezone('utc', now()),
    'summary', jsonb_build_object(
      'totalLeads', summary.total_leads,
      'uniqueVisitors', summary.unique_visitors,
      'formConversion',
        case
          when summary.unique_visitors > 0
            then round(((summary.total_leads::numeric / summary.unique_visitors::numeric) * 100), 1)
          else 0
        end,
      'ctaClicks', summary.cta_clicks
    ),
    'ctaBreakdown', coalesce((select data from cta_breakdown), '[]'::jsonb),
    'scrollDepth', coalesce((select data from scroll_breakdown), '[]'::jsonb)
  )
  into result
  from summary;

  return result;
end;
$$;

create or replace function public.get_recent_leads(limit_count int default 50)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  result jsonb;
begin
  perform public.assert_admin_access();

  select coalesce(
    jsonb_agg(
      jsonb_build_object(
        'id', leads.id,
        'created_at', leads.created_at,
        'name', leads.name,
        'phone', leads.phone,
        'telegram', leads.telegram,
        'contact', leads.contact,
        'email', leads.email,
        'comment', leads.comment,
        'source_context', leads.source_context,
        'page_path', leads.page_path,
        'referrer', leads.referrer,
        'utm_source', leads.utm_source,
        'utm_medium', leads.utm_medium,
        'utm_campaign', leads.utm_campaign
      )
      order by leads.created_at desc
    ),
    '[]'::jsonb
  )
  into result
  from (
    select *
    from public.leads
    order by created_at desc
    limit greatest(limit_count, 1)
  ) as leads;

  return result;
end;
$$;

