create or replace function public.is_admin_user()
returns boolean
language sql
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admin_users
    where lower(email) = lower(coalesce(auth.jwt() ->> 'email', ''))
  );
$$;

create or replace function public.assert_admin_access()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  if not public.is_admin_user() then
    raise exception 'admin access required';
  end if;
end;
$$;

create or replace function public.create_lead(payload jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  inserted_id uuid;
  input_name text := btrim(coalesce(payload ->> 'name', ''));
  input_phone text := btrim(coalesce(payload ->> 'phone', ''));
  input_telegram text := btrim(coalesce(payload ->> 'telegram', ''));
  input_contact text := btrim(coalesce(payload ->> 'contact', ''));
begin
  if input_name = '' then
    raise exception 'name is required';
  end if;

  if input_phone = '' then
    raise exception 'phone is required';
  end if;

  if input_telegram = '' then
    raise exception 'telegram is required';
  end if;

  if input_contact = '' then
    input_contact := concat_ws(' / ', input_phone, input_telegram);
  end if;

  insert into public.leads (
    name,
    contact,
    phone,
    telegram,
    email,
    comment,
    page_path,
    source_context,
    session_id,
    utm_source,
    utm_medium,
    utm_campaign,
    device_type,
    referrer
  )
  values (
    input_name,
    input_contact,
    input_phone,
    input_telegram,
    nullif(btrim(payload ->> 'email'), ''),
    nullif(btrim(payload ->> 'comment'), ''),
    nullif(payload ->> 'page_path', ''),
    nullif(payload ->> 'source_context', ''),
    nullif(payload ->> 'session_id', ''),
    nullif(payload ->> 'utm_source', ''),
    nullif(payload ->> 'utm_medium', ''),
    nullif(payload ->> 'utm_campaign', ''),
    nullif(payload ->> 'device_type', ''),
    nullif(payload ->> 'referrer', '')
  )
  returning id into inserted_id;

  return inserted_id;
end;
$$;

create or replace function public.upsert_feedback_vote(payload jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  inserted_id uuid;
  input_session_id text := btrim(coalesce(payload ->> 'session_id', ''));
  input_interest_level text := btrim(coalesce(payload ->> 'interest_level', ''));
begin
  if input_session_id = '' then
    raise exception 'session_id is required';
  end if;

  if input_interest_level = '' then
    raise exception 'interest_level is required';
  end if;

  insert into public.feedback_votes (
    session_id,
    interest_level,
    page_path,
    utm_source,
    utm_medium,
    utm_campaign,
    device_type,
    referrer
  )
  values (
    input_session_id,
    input_interest_level,
    nullif(payload ->> 'page_path', ''),
    nullif(payload ->> 'utm_source', ''),
    nullif(payload ->> 'utm_medium', ''),
    nullif(payload ->> 'utm_campaign', ''),
    nullif(payload ->> 'device_type', ''),
    nullif(payload ->> 'referrer', '')
  )
  on conflict (session_id) do update
  set
    interest_level = excluded.interest_level,
    page_path = excluded.page_path,
    utm_source = excluded.utm_source,
    utm_medium = excluded.utm_medium,
    utm_campaign = excluded.utm_campaign,
    device_type = excluded.device_type,
    referrer = excluded.referrer,
    updated_at = timezone('utc', now())
  returning id into inserted_id;

  return inserted_id;
end;
$$;

